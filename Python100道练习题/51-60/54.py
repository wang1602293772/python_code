#取一个整数a从右端开始的4〜7位。
'''
可以通过以下步骤获取一个整数a从右端开始的4～7位：
通过右移运算符 >> 将整数 a 的二进制数向右移动 4 位，将待获取的 4 位移动到最右边。
通过按位与运算符 & 和一个掩码（二进制数 1111，即十进制数 15）进行按位与运算，从而保留这 4 位二进制数，并将其他位清零。
（可选）通过左移运算符 << 将结果向左移动 4 位，将这 4 位移回原来的位置。
在这个例子中，整数 a 的二进制表示为 111010110111100110100010101，将其右移 4 位得到 111010110111100110100010，
然后与掩码 1111 进行按位与运算得到二进制数 1000，即十进制数 8。最后的结果需要再左移 4 位，得到十进制数 12，即这个整数从右端开始的 4～7 位。
'''
a = 123456789  # 待操作的整数
mask = 15      # 二进制数 1111，用于按位与运算
result = (a >> 4) & mask  # 获取从右端开始的 4~7 位
# 或者
# result = (a & (mask << 4)) >> 4
print(result)  # 输出结果，应该为 12